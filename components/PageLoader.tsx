function getRandomEmoji() {
  return EMOJIS[Math.floor(Math.random() * EMOJIS.length)];
}

export default function PageLoader() {
  const [isLoading, setIsLoading] = useState(true);
  const [emoji, setEmoji] = useState('');
  const [showEmoji, setShowEmoji] = useState(true);

  useEffect(() => {
    // Check if current path is /ship or /ship/form
    const isShipPage = window.location.pathname.startsWith('/ship');
    setShowEmoji(!isShipPage);

    // Pick completely random emoji
    const randomEmoji = getRandomEmoji();
    setEmoji(randomEmoji);

    // Store emoji in sessionStorage so EasterEgg can access it
    sessionStorage.setItem('currentEmoji', randomEmoji);

    // Hide loader when page is ready
    const timer = setTimeout(() => {
      setIsLoading(false);
    }, 1500);

    // Also check if document is ready
    if (document.readyState === 'complete') {
      setTimeout(() => setIsLoading(false), 800);
      clearTimeout(timer);
    }

    return () => clearTimeout(timer);
  }, []);

  return (
    <AnimatePresence mode="wait">
      {isLoading && (
        <motion.div
          initial={{ opacity: 1 }}
          exit={{ opacity: 0 }}
          transition={{ duration: 0.4, ease: [0.19, 1, 0.22, 1] }}
          className="fixed inset-0 z-[9999] flex items-center justify-center bg-background"
        >
          {/* Emoji animation - smaller size - hide on /ship page */}
          {showEmoji && (
            <motion.div
              initial={{ scale: 0.3, opacity: 0, rotate: -180 }}
              animate={{
                scale: [0.3, 0.7, 0.5],
                opacity: 1,
                rotate: 0,
              }}
              exit={{
                scale: 0.8,
                opacity: 0,
                rotate: 180,
              }}
              transition={{
                duration: 0.6,
                ease: [0.19, 1, 0.22, 1],
              }}
              className="text-[6rem]"
            >
              {emoji}
            </motion.div>
          )}
        </motion.div>
      )}
    </AnimatePresence>
  );
}
